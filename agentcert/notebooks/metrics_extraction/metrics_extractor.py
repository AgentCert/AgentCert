async def extract_metrics_from_multiple_files_with_llm(
    file_paths: List[str],
) -> List[MetricsExtractionResult]:
    """
    Extract metrics from multiple files using LLM.

    Args:
        file_paths: List of paths to agent run report files.

    Returns:
        List of MetricsExtractionResult objects.
    """
    llm_client = AzureLLMClient()
    try:
        results = []
        for file_path in file_paths:
            result = await extract_metrics_with_llm(file_path, llm_client)
            results.append(result)
        return results
    finally:
        await llm_client.close()


