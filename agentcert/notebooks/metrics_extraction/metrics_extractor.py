async def extract_metrics_with_llm(
    file_path: str, llm_client: Optional[AzureLLMClient] = None
):
    """
    Extract metrics from a file using LLM (recommended).

    Args:
        file_path: Path to the agent run report file.
        llm_client: Optional shared LLM client.

    Returns:
        MetricsExtractionResult containing the extracted metrics.
    """
